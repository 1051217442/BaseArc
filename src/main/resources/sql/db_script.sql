CREATE TABLE SYS_EMPLOYEE (
  _ID VARCHAR(32) PRIMARY KEY,
  _NAME VARCHAR(100),
  _EMAIL VARCHAR(100),
  _TEL VARCHAR(20),
  _NO VARCHAR(20),
  _ICON_ID VARCHAR(200),

  _ACCOUNT VARCHAR(50),
  _PASSWORD VARCHAR(255),
  _SALT VARCHAR(20),
  _STATUS INT
);

